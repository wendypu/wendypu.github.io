function setupGameCards() {
  if (!gameDeck || !gameDialog || !gameDialogContent) return;

  const closeButton = gameDialog.querySelector(".game-dialog-close");
  const desktopCards = window.matchMedia("(min-width: 921px)");
  let activeDominoIndex = null;

  const resetDominoCards = () => {
    activeDominoIndex = null;
    gameDeck.querySelectorAll(".game-card").forEach((card, index) => {
      card.classList.remove("is-domino-main", "is-domino-near", "is-domino-far");
      card.style.setProperty("--domino-delay", "0ms");
      card.style.zIndex = String(index + 1);
    });
  };

  const applyDominoCards = (activeIndex) => {
    if (!desktopCards.matches || activeDominoIndex === activeIndex) return;
    activeDominoIndex = activeIndex;

    gameDeck.querySelectorAll(".game-card").forEach((card, index) => {
      const distance = Math.abs(index - activeIndex);
      card.classList.remove("is-domino-main", "is-domino-near", "is-domino-far");
      card.style.setProperty("--domino-delay", `${Math.min(distance, 2) * 22}ms`);

      if (distance === 0) {
        card.classList.add("is-domino-main");
        card.style.zIndex = "32";
      } else if (distance === 1) {
        card.classList.add("is-domino-near");
        card.style.zIndex = "24";
      } else if (distance === 2) {
        card.classList.add("is-domino-far");
        card.style.zIndex = "18";
      } else {
        card.style.zIndex = String(index + 1);
      }
    });
  };

  gameDeck.addEventListener("pointerover", (event) => {
    const card = event.target.closest(".game-card");
    if (!card || !gameDeck.contains(card)) return;
    applyDominoCards(Number(card.dataset.gameIndex));
  });

  gameDeck.addEventListener("pointerleave", resetDominoCards);

  gameDeck.addEventListener("focusin", (event) => {
    const card = event.target.closest(".game-card");
    if (!card || !gameDeck.contains(card)) return;
    applyDominoCards(Number(card.dataset.gameIndex));
  });

  gameDeck.addEventListener("focusout", (event) => {
    if (!gameDeck.contains(event.relatedTarget)) resetDominoCards();
  });

  window.addEventListener("resize", () => {
    resetDominoCards();
    scheduleGameLayout();
  });

  gameDeck.addEventListener("click", (event) => {
    const card = event.target.closest("[data-game-index]");
    if (!card) return;

    const index = Number(card.dataset.gameIndex);
    const game = gameCards[index];
    if (!game) return;

    gameDialogContent.innerHTML = gameCardMarkup(game, index, true);
    gameDialog.showModal();
  });

  closeButton?.addEventListener("click", () => gameDialog.close());

  gameDialog.addEventListener("click", (event) => {
    if (event.target === gameDialog) gameDialog.close();
  });
}

