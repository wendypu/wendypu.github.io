function formatMetric(value, decimals) {
  return value.toLocaleString("en-US", {
    minimumFractionDigits: decimals,
    maximumFractionDigits: decimals
  });
}

function animateMetricNumber(numberNode) {
  const target = Number(numberNode.dataset.value || 0);
  const suffix = numberNode.dataset.suffix || "";
  const decimals = Number(numberNode.dataset.decimals || 0);
  const startValue = target > 1 ? 1 : 0;
  const duration = 820;
  const startTime = performance.now();

  const tick = (time) => {
    const progress = Math.min((time - startTime) / duration, 1);
    const eased = 1 - Math.pow(1 - progress, 3);
    const current = startValue + (target - startValue) * eased;
    numberNode.textContent = `${formatMetric(current, decimals)}${suffix}`;
    if (progress < 1) requestAnimationFrame(tick);
  };

  requestAnimationFrame(tick);
}

