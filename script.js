function setupPersonaPanel() {
  const panel = document.querySelector(".persona-panel");
  const stage = document.querySelector(".persona-stage");
  const figure = document.querySelector("#personaFigure");
  const image = document.querySelector("#personaDetailImage");
  const kicker = document.querySelector("#personaDetailKicker");
  const title = document.querySelector("#personaDetailTitle");
  const text = document.querySelector("#personaDetailText");
  const tags = document.querySelectorAll(".persona-tag");
  const popup = document.querySelector(".persona-popup");
  let hideTimer;
  let revealTimer;
  let restoreTimer;
  if (!panel || !stage || !figure || !image || !kicker || !title || !text || !popup || !tags.length) return;

  const defaultFigure = {
    src: figure.getAttribute("src"),
    alt: figure.getAttribute("alt"),
  };
  let activeTag = null;

  const activate = (button) => {
    window.clearTimeout(hideTimer);
    window.clearTimeout(revealTimer);
    window.clearTimeout(restoreTimer);
    activeTag = button;
    const label = button.textContent.trim();
    tags.forEach((item) => {
      const isActive = item === button;
      item.classList.toggle("is-active", isActive);
      item.setAttribute("aria-selected", String(isActive));
    });
    figure.style.opacity = "0";
    revealTimer = window.setTimeout(() => {
      figure.src = button.dataset.image;
      figure.alt = `${label}相关画面`;
      image.src = button.dataset.image;
      image.alt = `${label}相关画面`;
      kicker.textContent = label;
      title.textContent = "关键词说明";
      text.textContent = button.dataset.text;
      stage.dataset.persona = button.dataset.persona || "";
      stage.classList.add("is-detail-active");
      figure.style.opacity = "1";
      popup.classList.add("is-visible");
      popup.setAttribute("aria-hidden", "false");
    }, 120);
  };

  const hide = () => {
    window.clearTimeout(hideTimer);
    window.clearTimeout(revealTimer);
    window.clearTimeout(restoreTimer);
    activeTag = null;
    tags.forEach((item) => {
      item.classList.remove("is-active");
      item.setAttribute("aria-selected", "false");
    });
    hideTimer = window.setTimeout(() => {
      popup.classList.remove("is-visible");
      popup.setAttribute("aria-hidden", "true");
      figure.style.opacity = "0";
      restoreTimer = window.setTimeout(() => {
        if (activeTag) return;
        figure.src = defaultFigure.src;
        figure.alt = defaultFigure.alt;
        stage.classList.remove("is-detail-active");
        delete stage.dataset.persona;
        figure.style.opacity = "1";
      }, 120);
    }, 80);
  };

  tags.forEach((button) => {
    button.addEventListener("click", (event) => {
      event.stopPropagation();
      if (stage.classList.contains("is-detail-active") && activeTag === button) {
        hide();
        return;
      }
      activate(button);
    });
  });

  document.addEventListener("click", (event) => {
    if (!activeTag || panel.contains(event.target)) return;
    hide();
  });

  panel.addEventListener("pointermove", (event) => {
    const rect = panel.getBoundingClientRect();
    const x = (event.clientX - rect.left) / rect.width - 0.5;
    const y = (event.clientY - rect.top) / rect.height - 0.5;
    panel.style.setProperty("--tilt-x", `${(-y * 4).toFixed(2)}deg`);
    panel.style.setProperty("--tilt-y", `${(x * 4).toFixed(2)}deg`);
  });

  panel.addEventListener("pointerleave", () => {
    panel.style.setProperty("--tilt-x", "0deg");
    panel.style.setProperty("--tilt-y", "0deg");
  });
}

