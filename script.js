function layoutGameCards() {
  if (!gameDeck) return;

  const cards = [...gameDeck.querySelectorAll(".game-card")];
  if (!cards.length || window.matchMedia("(max-width: 920px)").matches) return;

  const deckWidth = gameDeck.getBoundingClientRect().width || 1074;
  const cardWidth = cards[0].getBoundingClientRect().width || 190;
  const cardHeight = cardWidth * 1468 / 951;
  const edgeGap = 8;
  const maxRotate = 8;
  const edgeAngle = Math.PI * maxRotate / 180;
  const edgeWidth = cardWidth * Math.cos(edgeAngle) + cardHeight * Math.sin(edgeAngle);
  const spread = Math.max(0, deckWidth - edgeGap * 2 - edgeWidth);
  const centerOffset = spread / 2;
  const lastIndex = Math.max(cards.length - 1, 1);

  cards.forEach((card, index) => {
    const progress = cards.length === 1 ? 0 : (index / lastIndex) * 2 - 1;
    const x = progress * centerOffset;
    const y = -18 + Math.abs(progress) * 54;
    const rotate = progress * maxRotate;

    card.style.setProperty("--card-x", `${x.toFixed(1)}px`);
    card.style.setProperty("--card-y", `${y.toFixed(1)}px`);
    card.style.setProperty("--card-rotate", `${rotate.toFixed(2)}deg`);
    card.style.zIndex = String(index + 1);
  });
}

function scheduleGameLayout() {
  window.cancelAnimationFrame(gameLayoutFrame);
  gameLayoutFrame = window.requestAnimationFrame(layoutGameCards);
}

